public class PrimitiveDataTypes {
    public static void main(String[] args) {
        // Byte data type
        byte byteValue = 100;
        System.out.println("Byte Value: " + byteValue + " | Type: byte");
        
        // Short data type
        short shortValue = 10000;
        System.out.println("Short Value: " + shortValue + " | Type: short");
        
        // Int data type
        int intValue = 100000;
        System.out.println("Int Value: " + intValue + " | Type: int");
        
        // Long data type
        long longValue = 1000000000L;
        System.out.println("Long Value: " + longValue + " | Type: long");
        
        // Float data type
        float floatValue = 3.14f;
        System.out.println("Float Value: " + floatValue + " | Type: float");
        
        // Double data type
        double doubleValue = 3.14159;
        System.out.println("Double Value: " + doubleValue + " | Type: double");
        
        // Char data type
        char charValue = 'A';
        System.out.println("Char Value: " + charValue + " | Type: char");
        
        // Boolean data type
        boolean booleanValue = true;
        System.out.println("Boolean Value: " + booleanValue + " | Type: boolean");
    }
}